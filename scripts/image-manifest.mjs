function slugify(value) {
  return value
    .normalize("NFKD")
    .toLowerCase()
    .replace(/\.[^.]+$/, "")
    .replace(/&/g, " and ")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-|-$/g, "");
}

function humanize(source, album) {
  const filename = source.split("/").at(-1).replace(/\.[^.]+$/, "").trim();
  const normalized = filename.toLowerCase();

  if (titleOverrides[normalized]) {
    return titleOverrides[normalized];
  }

  if (/^sci \d+$/i.test(filename)) {
    return `Practical science activity ${filename.match(/\d+/)[0]}`;
  }

  if (/^year group/i.test(filename)) {
    return filename
      .replace(/^year group/i, "Divine year group")
      .replace(/\s+/g, " ")
      .trim();
  }

  if (/^a\d+$/i.test(filename)) {
    return `Award moment ${filename.slice(1)}`;
  }

  if (/^m\d+$/i.test(filename) && album === "cultural-play") {
    return `Cultural performance ${filename.slice(1)}`;
  }

  if (/^m\d+$/i.test(filename)) {
    return `Creative presentation ${filename.slice(1)}`;
  }

  if (/^[ps]\d+$/i.test(filename)) {
    return `Cultural performance ${filename.slice(1)}`;
  }

  if (/^stage \d+$/i.test(filename)) {
    return `Stage recital ${filename.match(/\d+/)[0]}`;
  }

  if (/^student\d*$/i.test(filename)) {
    const number = filename.match(/\d+/)?.[0];
    return number ? `Graduation portrait ${number}` : "Graduation portrait";
  }

  if (/^student speaking\s*\d*$/i.test(filename)) {
    const number = filename.match(/\d+/)?.[0];
    return number ? `Student speaker ${number}` : "Student speaker";
  }

  if (/^g\d+$/i.test(filename)) {
    return `Graduation group ${filename.match(/\d+/)[0]}`;
  }

  if (/^c\d+$/i.test(filename)) {
    return `Graduation class group ${filename.match(/\d+/)[0]}`;
  }

  if (/^dance\d+$/i.test(filename)) {
    return `Dance performance ${filename.match(/\d+/)[0]}`;
  }

  if (/^group\d+$/i.test(filename)) {
    return `Competition group ${filename.match(/\d+/)[0]}`;
  }

  return filename
    .replace(/[-_]+/g, " ")
    .replace(/\s+/g, " ")
    .replace(/\b\w/g, (character) => character.toUpperCase());
}

function pageUsages(source) {
  return Object.entries(siteImageAliases)
    .filter(([, aliasSource]) => aliasSource === source)
    .map(([alias]) => alias);
}

export const imageManifest = imageGroups.flatMap((group) =>
  group.sources.map((source) => {
    const title = humanize(source, group.album);

    return {
      id: slugify(source),
      source,
      album: group.album,
      title,
      alt: `${albumAltPrefixes[group.album]} ${title.toLowerCase()}.`,
      caption: title,
      position: "center",
      fit: "cover",
      profile: heroSources.has(source) ? "hero" : "gallery",
      approved: true,
      usages: pageUsages(source),
    };
  }),
);
